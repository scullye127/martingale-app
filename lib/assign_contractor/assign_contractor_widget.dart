    super.initState();
    _model = createModel(context, () => AssignContractorModel());

    logFirebaseEvent('screen_view',
        parameters: {'screen_name': 'assignContractor'});
    // On page load action.
    SchedulerBinding.instance.addPostFrameCallback((_) async {
      logFirebaseEvent('ASSIGN_CONTRACTOR_assignContractor_ON_IN');
      logFirebaseEvent('assignContractor_firestore_query');
      _model.contractors = await queryUsersRecordOnce(
        queryBuilder: (usersRecord) => usersRecord.where(
          'user_role',
          isEqualTo: 'contractors',
        ),
      );
